package com.kazdo.true_false_test;

public class NumberCheck 
{
    public static boolean isNegative(int n) {
    	if(n < 0)
    		return true;
    	else
    		return false;
    }
    
    public static boolean isPositive(int n) {
    	if(n > 0)
    		return true;
    	else
    		return false;
    }
    
    public static boolean isZero(int n) {
    	if(n == 0)
    		return true;
    	else
    		return false;
    }
    
}
